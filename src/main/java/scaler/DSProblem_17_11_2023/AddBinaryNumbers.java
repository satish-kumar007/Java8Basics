public class AddBinaryNumbers {
    public static void main(String[] args) {

        int a = 27;
        System.out.println(a&1);
        System.out.println(a&a);
        System.out.println(a|a);
        System.out.println(a^a);
        System.out.println(a^1);
    }
}
