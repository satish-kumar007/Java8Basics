package scaler.problems16092023;

public class MultipleChoice {
    public static void main(String[] args) {
        for(int i = 10; i > 0; ) {
            i--;
            System.out.print(i + " ");
        }

        for(int i = 1; i <= 10; i++) {
            System.out.print(i + 1 + " ");
            i++;
        }
        System.out.println();
        for(int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
            i++;
        }

    }
}
